async function seedTrainingData() {
  if (!process.env.DATABASE_URL) {
    console.log("DATABASE_URL not set, skipping seed");
    return;
  }

  try {
    const db = drizzle(process.env.DATABASE_URL);

    // Get the owner user (first admin user)
    const ownerUser = await db
      .select()
      .from(users)
      .where(eq(users.role, "admin"))
      .limit(1);

    if (ownerUser.length === 0) {
      console.log("No admin user found, skipping seed");
      return;
    }

    const ownerId = ownerUser[0].id;

    // Check if training data already exists for this user
    const existingData = await db
      .select()
      .from(training_data)
      .where(eq(training_data.userId, ownerId))
      .limit(1);

    if (existingData.length > 0) {
      console.log(
        `Training data already exists for user ${ownerId}, skipping seed`
      );
      return;
    }

    // Insert training data
    for (const item of trainingDataset) {
      await db.insert(training_data).values({
        userId: ownerId,
        question: item.question,
        answer: item.answer,
      });
    }

    console.log(
      `✓ Seeded ${trainingDataset.length} training data items for user ${ownerId}`
    );
  } catch (error) {
    console.error("Error seeding training data:", error);
    process.exit(1);
  }
}

seedTrainingData();
